package data_structure_Array_16_12;

public class Player {
    int jerseyNo;
    String name;
    int runs;
    int wickets;
    int matches;

    public Player(int jerseyNo, String name, int runs, int wickets, int matches) {
        this.jerseyNo = jerseyNo;
        this.name = name;
        this.runs = runs;
        this.wickets = wickets;
        this.matches = matches;
    }
}
